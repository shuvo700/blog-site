    public function add($post)
    {
        $user=\Auth::user();
        $is_favorite= $user->favorite_post()->where('post_id',$post)->count();
        if($is_favorite==0){
            $user->favorite_post()->attach($post);
            session()->flash('favorite', 'This Post Added To Your Favorite List');
            return redirect()->back();
        }
        else {
            $user->favorite_post()->detach($post);
            session()->flash('favorite', 'Remove This Post From Your Favorite List');
            return redirect()->back();
        }
    }

